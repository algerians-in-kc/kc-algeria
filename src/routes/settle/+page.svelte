	<!-- Step 1 -->
	<div class="bg-white rounded-xl border border-gray-100 p-6">
		<div class="flex items-center gap-3 mb-4">
			<span class="w-8 h-8 bg-green-100 text-green-700 rounded-full flex items-center justify-center font-bold text-sm">1</span>
			<h2 class="text-xl font-bold text-gray-900">Social Security Number (SSN)</h2>
		</div>
		<p class="text-gray-600 mb-3">Your SSN is the foundation of everything in the US — you need it for work, banking, and most official processes.</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li>Go to your local Social Security Administration office (Olathe: 150 W. Dennis Ave)</li>
			<li>Bring your passport, visa, and I-94 arrival record</li>
			<li>Card arrives by mail in 2–4 weeks</li>
			<li>Do this in your first week if you have work authorization</li>
		</ul>
	</div>

	<!-- Step 2 -->
	<div class="bg-white rounded-xl border border-gray-100 p-6">
		<div class="flex items-center gap-3 mb-4">
			<span class="w-8 h-8 bg-green-100 text-green-700 rounded-full flex items-center justify-center font-bold text-sm">2</span>
			<h2 class="text-xl font-bold text-gray-900">Driver's License / State ID</h2>
		</div>
		<p class="text-gray-600 mb-3">Kansas and Missouri both require you to get a local license. Your Algerian license is not valid long-term.</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li>Go to the Kansas DMV (Kansas residents) or Missouri DMV (MO residents)</li>
			<li>Bring: passport, SSN card, proof of address (utility bill or lease)</li>
			<li>You will need to pass a written and driving test</li>
			<li>Study the Kansas/Missouri driver's manual — available in the DMV office</li>
		</ul>
	</div>

	<!-- Step 3 -->
	<div class="bg-white rounded-xl border border-gray-100 p-6">
		<div class="flex items-center gap-3 mb-4">
			<span class="w-8 h-8 bg-green-100 text-green-700 rounded-full flex items-center justify-center font-bold text-sm">3</span>
			<h2 class="text-xl font-bold text-gray-900">Open a Bank Account</h2>
		</div>
		<p class="text-gray-600 mb-3">You can open a bank account even without a US credit history. Start here:</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li><strong>Bank of America</strong> and <strong>Chase</strong> both have accounts for newcomers</li>
			<li>Bring: passport + ITIN or SSN + proof of address</li>
			<li><strong>Credit Unions</strong> (like Meritrust or Mazuma) are often more welcoming to newcomers</li>
			<li>Start building credit immediately with a secured credit card</li>
		</ul>
	</div>

	<!-- Step 4 -->
	<div class="bg-white rounded-xl border border-gray-100 p-6">
		<div class="flex items-center gap-3 mb-4">
			<span class="w-8 h-8 bg-green-100 text-green-700 rounded-full flex items-center justify-center font-bold text-sm">4</span>
			<h2 class="text-xl font-bold text-gray-900">Housing</h2>
		</div>
		<p class="text-gray-600 mb-3">Renting without US credit history is the biggest challenge for newcomers. Here's how to navigate it:</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li>Offer 2–3 months rent upfront to overcome lack of credit history</li>
			<li>Ask a community member to co-sign if possible</li>
			<li>Look in Olathe and Gardner — more affordable than Overland Park</li>
			<li>Facebook Marketplace and Zillow are the most used platforms here</li>
			<li>Avoid leases longer than 1 year until you know the area well</li>
		</ul>
	</div>

	<!-- Step 5 -->
	<div class="bg-white rounded-xl border border-gray-100 p-6">
		<div class="flex items-center gap-3 mb-4">
			<span class="w-8 h-8 bg-green-100 text-green-700 rounded-full flex items-center justify-center font-bold text-sm">5</span>
			<h2 class="text-xl font-bold text-gray-900">Schools & Childcare</h2>
		</div>
		<p class="text-gray-600 mb-3">KC metro has excellent public schools. Your children can enroll immediately.</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li>Enroll at your local district school — bring immunization records and proof of address</li>
			<li>Olathe School District and Blue Valley (Overland Park) are highly rated</li>
			<li>ESL (English as Second Language) programs are available in most schools</li>
			<li>Childcare assistance available through KanCare for qualifying families</li>
		</ul>
	</div>

	<!-- Step 6 -->
	<div class="bg-white rounded-xl border border-gray-100 p-6">
		<div class="flex items-center gap-3 mb-4">
			<span class="w-8 h-8 bg-green-100 text-green-700 rounded-full flex items-center justify-center font-bold text-sm">6</span>
			<h2 class="text-xl font-bold text-gray-900">Health Insurance</h2>
		</div>
		<p class="text-gray-600 mb-3">Healthcare in the US is expensive without insurance. Getting covered is a priority.</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li>If employed, enroll in your employer's plan immediately</li>
			<li>Healthcare.gov marketplace — open enrollment or special enrollment after moving</li>
			<li>KanCare (Medicaid) for low-income families in Kansas</li>
			<li>Community Health Center of Southeast Kansas has sliding-scale fees</li>
		</ul>
	</div>

