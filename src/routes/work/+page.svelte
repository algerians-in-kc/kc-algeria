		<h2 class="text-xl font-bold text-gray-900 mb-4">📄 US Resume vs. Algerian CV</h2>
		<p class="text-gray-600 mb-3">The US resume is very different from what you're used to. Key differences:</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li>1 page max for most jobs (2 pages for senior roles)</li>
			<li>No photo, no age, no marital status — it's illegal to ask</li>
			<li>Lead with results, not duties: "Increased sales by 30%" not "Responsible for sales"</li>
			<li>Use American English spelling (labor not labour, etc.)</li>
			<li>LinkedIn profile is as important as your resume here</li>
		</ul>
	</div>

	<div class="bg-white rounded-xl border border-gray-100 p-6">
		<h2 class="text-xl font-bold text-gray-900 mb-4">🎓 Getting Your Degree Recognized</h2>
		<p class="text-gray-600 mb-3">Your Algerian degree is valid but may need evaluation for some roles.</p>
		<ul class="text-sm text-gray-600 space-y-2 list-disc list-inside">
			<li>Use a credential evaluation service: WES (wes.org) or ECE (ece.org)</li>
			<li>Costs around $200 and takes 4–8 weeks</li>
			<li>Required for healthcare, engineering, education, and government jobs</li>
			<li>IT and tech roles often don't require evaluation — skills matter more</li>
		</ul>
	</div>

	<div class="bg-white rounded-xl border border-gray-100 p-6">
