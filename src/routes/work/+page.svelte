			<li><strong>LinkedIn</strong> — most important platform, keep it updated</li>
			<li><strong>Indeed.com</strong> — largest job board in the US</li>
			<li><strong>Glassdoor</strong> — jobs + company reviews and salaries</li>
			<li><strong>KC major employers:</strong> Cerner/Oracle, Sprint/T-Mobile, Garmin, Hallmark, H&R Block</li>
			<li>Johnson County (Overland Park/Olathe area) has a strong IT and engineering job market</li>
